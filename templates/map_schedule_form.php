    <h2 id="toc_170">Academic Honesty</h2>

    <p>This course&#39;s philosophy on academic honesty is best stated as &quot;be reasonable.&quot;  The course recognizes that interactions with classmates and others can facilitate mastery of the course&#39;s material. However, there remains a line between enlisting the help of another and submitting the work of another. This policy characterizes both sides of that line.</p>

    <p>The essence of all work that you submit to this course must be your own. Collaboration on problem sets is not permitted except to the extent that you may ask classmates and others for help so long as that help does not reduce to another doing your work for you.  Generally speaking, when asking for help, you may show your code to others, but you may not view theirs, so long as you and they respect this policy&#39;s other constraints.  Collaboration on the course&#39;s final project is permitted to the extent prescribed by its specification.</p>

    <p>Below are rules of thumb that (inexhaustively) characterize acts that the course considers reasonable and not reasonable.  If in doubt as to whether some act is reasonable, do not commit it.</p>

    <h3 id="toc_171">Reasonable</h3>

    <ul>
    <li>Communicating with classmates about problem sets&#39; problems in English (or some other spoken language).</li>
    <li>Discussing the course&#39;s material with others in order to understand it better.</li>
    <li>Helping a classmate identify a bug in his or her code, as by viewing, compiling, or running his or her code, even on your own computer.</li>
    <li>Incorporating snippets of code that you find online or elsewhere into your own code, provided that those snippets are not themselves solutions to assigned problems and that you cite the snippets&#39; origins.</li>
    <li>Sending or showing code that you&#39;ve written to someone, possibly a classmate, so that he or she might help you identify and fix a bug.</li>
    <li>Sharing snippets of your own code on Reddit or elsewhere so that others might help you identify and fix a bug.</li>
    <li>Turning to the web or elsewhere for instruction beyond the course&#39;s own, for references, and for solutions to technical difficulties, but not for outright solutions to problem set&#39;s problems or your own final project.</li>
    <li>Whiteboarding solutions to problem sets with others using diagrams or pseudocode but not actual code.</li>
    <li>Working with (and even paying) a tutor to help you with the course, provided the tutor does not do your work for you.</li>
    </ul>

    <h3 id="toc_172">Not Reasonable</h3>

    <ul>
    <li>Accessing a solution in CS50 Vault to some problem prior to (re-)submitting your own.</li>
    <li>Asking a classmate to see his or her solution to a problem set&#39;s problem before (re-)submitting your own.</li>
    <li>Decompiling, deobfuscating, or disassembling the staff&#39;s solutions to problem sets.</li>
    <li>Failing to cite (as with comments) the origins of code or techniques that you discover outside of the course&#39;s own lessons and integrate into your own work, even while respecting this policy&#39;s other constraints.</li>
    <li>Giving or showing to a classmate your solution to a problem set&#39;s problem when it is he or she, and not you, who is struggling to solve it.</li>
    <li>Paying or offering to pay an individual for work that you may submit as (part of) your own.</li>
    <li>Providing or making available solutions to problem sets to individuals who might take this course in the future.</li>
    <li>Searching for or soliciting outright solutions to problem sets online or elsewhere.</li>
    <li>Splitting a problem set&#39;s workload with another individual and combining your work.</li>
    <li>Submitting (after possibly modifying) the work of another individual beyond allowed snippets.</li>
    <li>Submitting the same or similar work to this course that you have submitted or will submit to another.</li>
    <li>Viewing another&#39;s solution to a problem set&#39;s problem and basing your own solution on it.</li>
    </ul>

