    <h2 id="toc_168">Final Project</h2>

    <p>The climax of this course is its final project.  The final project is your opportunity to take your newfound savvy with programming out for a spin and develop your very own piece of software.  So long as your project draws upon this course&#39;s lessons, the nature of your project is entirely up to you.  You may implement your project in any language(s).  You are welcome to utilize infrastructure other than the CS50 Appliance.  All that we ask is that you build something of interest to you, that you solve an actual problem, or that you change the world.  Strive to create something that outlives this course.</p>

    <p>Inasmuch as software development is rarely a one-person effort, you are allowed an opportunity to collaborate with one or two classmates for this final project.  Needless to say, it is expected that every student in any such group contribute equally to the design and implementation of that group&#39;s project.  Moreover, it is expected that the scope of a two- or three-person group&#39;s project be, respectively, twice or thrice that of a typical one-person project.  A one-person project, mind you, should entail more time and effort than is required by each of the course&#39;s problem sets.  </p>

    <p>See <a href="http://cdn.cs50.net/2014/x/project/project.html">http://cdn.cs50.net/2014/x/project/project.html</a> for details.</p>

